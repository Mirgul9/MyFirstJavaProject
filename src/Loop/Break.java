package Loop;

public class Break {
	public static void main(String[] args) {
		
		for( int i = 0; i < 50; i ++) {
			System.out.println("Hello");
			// Break the loop after " hello " printed 13 times
			if( i==12) {
				/// if you want close u need put break
			break;
		
		
		}
	}

}
}